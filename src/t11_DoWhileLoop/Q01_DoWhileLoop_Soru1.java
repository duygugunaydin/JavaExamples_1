package t11_DoWhileLoop;

public class Q01_DoWhileLoop_Soru1 {
    public static void main(String[] args) {

        // Soru 1 - ’k’ harfinden ‘t’ harfine kadar harfleri yazdirin.

        char krk = 'k';


        do {
            System.out.print( krk + " ");
            krk++;
        }while (krk<='t');


    }

}
