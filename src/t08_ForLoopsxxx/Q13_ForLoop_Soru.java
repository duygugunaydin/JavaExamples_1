package t08_ForLoopsxxx;

public class Q13_ForLoop_Soru {

    public static void main(String[] args) {

        /*
        Soru 25-) Alfabeyi konsola yazdırın.
                  örnek:
                  a b c .. .. .. .. y z
                  ipucu: char kullanmak iyi bir fikir olabilir
         */

        for (char i = 'a'; i <='z' ; i++) {
            System.out.print( i + " ");
        }

    }
}
